package com.imtf.library;

public class Requirement {
	
	/*
	 * We as IMTF want to have a new IMTF Library Application. Therefore we’d like
	 * you to design and implement a simple REST API which uses JSON as the payload
	 * transport with the following user stories:
	 * 
	 * A user should be able to list all books order by e.g. (date of publication,
	 * alphabetically), maybe the results should be limited
	 * 
	 * A user should be able to add new books or edit information of an existing
	 * book.
	 * 
	 * Of course a user needs to be able to search for a book.
	 * 
	 * Only an admin is allowed to remove a book from the library
	 * 
	 * There are no restrictions on how the API endpoints need to be defined.
	 * 
	 * The book information should be stored at some point in a database (MySQL
	 * PostgreSQL, MongoDB, Neo4J, SQLite, etc.), but for the beginning we’d keep
	 * the information simply in- memory.
	 * 
	 * Finally extend the application to use a real persistence layer, maybe with
	 * the use of an ORM.
	 */

}
